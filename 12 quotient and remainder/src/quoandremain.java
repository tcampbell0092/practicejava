public class quoandremain {
    public static void main(String[]args){
        // defines numbers to be divided
        int num1 = 15, num2 = 2;
        // output for quotient
        int quotient = num1 / num2;
        // output for remainder
        int remainder = num1 % num2;

        // prints out operations
        System.out.println("Quotient of these numbers is: " + quotient);
        System.out.println("Remainder of these numbers is: " + remainder);
    }
}
